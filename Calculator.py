while True:
    number_1 = int(input("Enter your first number:"))
    number_2 = int(input("Enter your second number:"))

    print("Sum: ".format(number_1, number_2))
    print(number_1 + number_2)

    print("Difference: ".format(number_1, number_2))
    print(number_1 - number_2)

    print("Product ".format(number_1, number_2))
    print(number_1 * number_2)

    print("Division: ".format(number_1, number_2))
    print(number_1 / number_2)